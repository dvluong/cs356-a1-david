public class Student {
	private int id;
	protected char answer;
	
	public Student(int id){
		this.setId(id);
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public void chooseAnswer(){
		this.answer = (char) ((int) (Math.random() * 4) + 65);	
	}

}
